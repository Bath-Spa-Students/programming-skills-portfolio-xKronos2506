#input statement
alien_color = input("What is the alien color? ")

#color variable
alien_color_1 = "green"
alien_color_2 = "yellow"
alien_color_3 = "red"

#if-elif-else statement with output
if alien_color == alien_color_1:
    print("You just scored 5 points!")
elif alien_color == alien_color_2:
    print("You just earned 10 points!")
elif alien_color == alien_color_3:
    print("YOu just earned 15 points!")
else:
    print("You just earned 2 points!")
